'''

formatação com f-strings
x ou X = hexadecimal
(caractere)(<>^)(quantidade)(. quantas casas decimais)


'''

variavel = 'abc'
print(f'{variavel}')
print(f'{variavel:<10}') #alinha para a esquerda
print(f'{variavel:>10}') #alinha para a direita
print(f'{variavel:^10}') #alinha ao centro
print(f'{variavel:$^10}') #alinha ao centro